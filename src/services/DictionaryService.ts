  public static lookupWord(word: string): string | null {
    return this.dictionary[word.toLowerCase()] || null;
  }

  public static addWord(word: string, meaning: string): void {
    this.dictionary[word.toLowerCase()] = meaning;
  }

  public static getAllWords(): string[] {
    return Object.keys(this.dictionary);
  }

  public static removeWord(word: string): void {
    delete this.dictionary[word.toLowerCase()];
  }

  public static exportDictionary(): Record<string, string> {
    return { ...this.dictionary };
  }

  public static importDictionary(importedData: Record<string, string>): number {
    let importCount = 0;
    
    for (const [word, meaning] of Object.entries(importedData)) {
      if (typeof word === 'string' && typeof meaning === 'string') {
        this.dictionary[word.toLowerCase()] = meaning;
        importCount++;
      }
    }
    
    return importCount;
  }
